// å‘é€æ¶ˆæ¯
const sendMessage = async () => {
  if (!newMessage.value.trim() || isLoading.value) return;

  const userMessage = newMessage.value.trim();

  // æ·»åŠ ç”¨æˆ·æ¶ˆæ¯
  messages.value.push({
    sender: 'user',
    text: userMessage
  });

  // æ·»åŠ åŠ©æ‰‹åŠ è½½æ¶ˆæ¯
  const assistantMessageIndex = messages.value.length;
